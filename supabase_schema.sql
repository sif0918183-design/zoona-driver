-- Basic Policies (Adjust as needed for specific security requirements)
-- Customers can read their own profile
CREATE POLICY "Customers see own profile" ON customers FOR SELECT USING (auth.uid() = id);
-- Drivers can read their own profile
CREATE POLICY "Drivers see own profile" ON drivers FOR SELECT USING (auth.uid() = id);

-- Public/Authenticated reads
CREATE POLICY "Anyone can see cities" ON cities FOR SELECT USING (TRUE);
CREATE POLICY "Anyone can see categories" ON vehicle_categories FOR SELECT USING (TRUE);

-- Ride Requests Policies
CREATE POLICY "Customers can insert requests" ON ride_requests FOR INSERT WITH CHECK (auth.uid() = customer_id);
CREATE POLICY "Customers can see own requests" ON ride_requests FOR SELECT USING (auth.uid() = customer_id);
CREATE POLICY "Drivers can see pending requests" ON ride_requests FOR SELECT USING (status = 'pending' OR driver_id = auth.uid());
CREATE POLICY "Drivers can update status to accept" ON ride_requests FOR UPDATE USING (status = 'pending' OR driver_id = auth.uid());

-- Rides Policies
CREATE POLICY "Participants can see ride" ON rides FOR SELECT USING (auth.uid() = passenger_id OR auth.uid() = driver_id);
CREATE POLICY "Drivers can insert/update rides" ON rides FOR ALL USING (auth.uid() = driver_id);

-- Chat Policies
CREATE POLICY "Participants can see chats" ON ride_chats FOR SELECT USING (
    EXISTS (SELECT 1 FROM rides WHERE id = ride_id AND (passenger_id = auth.uid() OR driver_id = auth.uid()))
    OR
    EXISTS (SELECT 1 FROM ride_requests WHERE id = ride_id AND (customer_id = auth.uid() OR driver_id = auth.uid()))
);
CREATE POLICY "Participants can insert chats" ON ride_chats FOR INSERT WITH CHECK (
    auth.uid() = sender_id
);

