-- Supabase Schema for Travel Platform

-- 1. Customers Table
CREATE TABLE IF NOT EXISTS customers (
    id UUID REFERENCES auth.users NOT NULL PRIMARY KEY,
    full_name TEXT NOT NULL,
    phone TEXT NOT NULL,
    phone2 TEXT,
    is_verified BOOLEAN DEFAULT FALSE,
    total_rides INTEGER DEFAULT 0,
    last_ride TIMESTAMP WITH TIME ZONE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 2. Drivers Table
CREATE TABLE IF NOT EXISTS drivers (
    id UUID REFERENCES auth.users NOT NULL PRIMARY KEY,
    full_name TEXT NOT NULL,
    phone TEXT NOT NULL,
    whatsapp_number TEXT,
    car_type TEXT,
    car_model TEXT,
    is_active BOOLEAN DEFAULT TRUE,
    verification_status TEXT DEFAULT 'pending',
    total_rides INTEGER DEFAULT 0,
    rating FLOAT DEFAULT 5.0,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 3. Cities Table
CREATE TABLE IF NOT EXISTS cities (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name_ar TEXT NOT NULL UNIQUE,
    is_active BOOLEAN DEFAULT TRUE
);

-- 4. Vehicle Categories Table
CREATE TABLE IF NOT EXISTS vehicle_categories (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name_ar TEXT NOT NULL,
    description_ar TEXT,
    price_multiplier FLOAT DEFAULT 1.0,
    icon_emoji TEXT DEFAULT '🚗',
    is_active BOOLEAN DEFAULT TRUE,
    sort_order INTEGER DEFAULT 0
);

-- 5. Ride Requests Table
CREATE TABLE IF NOT EXISTS ride_requests (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    customer_id UUID REFERENCES customers(id),
    passenger_name TEXT, -- Denormalized for easy display
    passenger_phone TEXT, -- Denormalized
    from_city TEXT NOT NULL,
    to_city TEXT NOT NULL,
    vehicle_category TEXT,
    estimated_price INTEGER,
    notes TEXT,
    status TEXT DEFAULT 'pending', -- 'pending', 'accepted', 'cancelled'
    driver_id UUID REFERENCES drivers(id),
    driver_name TEXT,
    driver_phone TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    accepted_at TIMESTAMP WITH TIME ZONE
);

-- 6. Rides Table
CREATE TABLE IF NOT EXISTS rides (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    passenger_id UUID REFERENCES customers(id),
    passenger_name TEXT,
    passenger_phone TEXT,
    driver_id UUID REFERENCES drivers(id),
    driver_name TEXT,
    driver_phone TEXT,
    from_city TEXT NOT NULL,
    to_city TEXT NOT NULL,
    vehicle_category TEXT,
    base_price INTEGER,
    final_price INTEGER,
    notes TEXT,
    status TEXT DEFAULT 'accepted', -- 'accepted', 'confirmed', 'completed', 'cancelled'
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    accepted_at TIMESTAMP WITH TIME ZONE
);

-- 7. Ride Chats Table
CREATE TABLE IF NOT EXISTS ride_chats (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ride_id BIGINT, -- Can reference ride_requests(id) or rides(id) depending on how we want it
    sender_id UUID NOT NULL,
    sender_name TEXT,
    sender_type TEXT, -- 'passenger' or 'driver'
    message TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 8. System Settings Table
CREATE TABLE IF NOT EXISTS system_settings (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    key TEXT NOT NULL UNIQUE,
    value TEXT
);

-- 9. Activity Logs Table
CREATE TABLE IF NOT EXISTS activity_logs (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID,
    user_type TEXT,
    action TEXT,
    description TEXT,
    related_id TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 10. Driver Notifications Table
CREATE TABLE IF NOT EXISTS driver_notifications (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    driver_id UUID REFERENCES drivers(id),
    title TEXT,
    message TEXT,
    type TEXT,
    is_read BOOLEAN DEFAULT FALSE,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

